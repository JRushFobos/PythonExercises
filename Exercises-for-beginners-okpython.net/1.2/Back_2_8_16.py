# Представление числа в 2-й СС.
num_bin = '011011101'
# Выведет 221 (10-я СС).        
print(int(num_bin, base=2))

# Представление числа в 8-й СС.
num_oct = '357'
# Выведет 239 (10-я СС).        
print(int(num_oct, base=8))        

# Представление числа в 16-й СС.
num_hex = 'AF23D'
# Выведет 717373 (10-я СС).        
print(int(num_hex, base=16))